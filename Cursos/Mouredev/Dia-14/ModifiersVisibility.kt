/*  
    Modificador Public
    Modificador de visibilidad por defecto.
    Declaraciones visibles y accesibles desde cualquier
    parte de nuestro codigo.
*/

/*
    Modificador Private
    Declaraciones visibles y accesibles solo desde
    el archivo en el que se encuentra la declaracion.
*/

/*
    Modificador Protected
    Comportamiento igual que private, exceptuando que el acceso
    no estara disponible para declaraciones de
    nivel superior y si para nivel inferior (subclases).
*/

/*
    Modificador Internal
    Declaraciones visibles y accesibles desde
    cualquier parte del modulo (nuestra app en
    este caso).
*/